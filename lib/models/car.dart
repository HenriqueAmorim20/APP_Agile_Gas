class Car{
  final String ownedByUid;
  final String marca;
  final String modelo;
  final int ano;
  final String motor;
  final String cor;
  final String placa;
  final String renevam;
  final int combustivel;

  Car({this.ownedByUid,
    this.marca,
    this.modelo,
    this.ano,
    this.motor,
    this.cor,
    this.placa,
    this.renevam,
    this.combustivel
  });
}